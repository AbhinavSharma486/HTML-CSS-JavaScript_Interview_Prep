// TODO : difference b/w box-sizing:border-box & box-sizing:content-box ?
// * Ans :- box-sizing: border-box" means that when you set the width and height of an element, it includes the padding and border within that specified size, while "box-sizing: content-box" (the default) means that the width and height only apply to the content itself, with padding and border added on top of that, making the element appear larger than the set dimensions

